function init(renderer) {
  parent.init(renderer);
  initEffects(renderer);
  stelar.addSwordAnimations(renderer, "em_wave_change.TASER", "skyhighocs:em_wave_change_sword", "taser");
  stelar.addAnimationWithData(renderer, "em_wave_change.OVER_CHARGE_AIM", "skyhighocs:em_wave_change_aim", "fiskheroes:beam_charge")
  .priority = 10;
};

function initEffects(renderer) {
  parent.initEffects(renderer);
  maskWaveChangingLights = renderer.createEffect("fiskheroes:overlay");
  maskWaveChangingLights.texture.set(null, "mask_wave_changing_lights");
  mask = renderer.createEffect("fiskheroes:overlay");
  mask.texture.set("mask", "mask_lights");
  stelar.bindBeam(renderer, "fiskheroes:lightning_cast", "skyhighocs:ecolyte_spark_shock", "rightArm", 0x00FFFF, [
    { "firstPerson": [-8.0, 4.5, -10.0], "offset": [-0.5, 9.0, 0.0], "size": [1.0, 1.0] }
  ]);
  stelar.bindBeam(renderer, "fiskheroes:energy_manipulation", "skyhighocs:ecolyte_spark_arc", "rightArm", 0x00FFFF, [
    { "firstPerson": [-4.5, 4.5, -20.0], "offset": [-0.5, 14.0, 0.0], "size": [2.0, 2.0] }
  ]);
  stelar.bindBeam(renderer, "fiskheroes:charged_beam", "skyhighocs:ecolyte_spark_over_charge", "rightArm", 0x00FFFF, [
    { "firstPerson": [-4.5, 3.75, -8.0], "offset": [-1.0, 9.0, -0.5], "size": [3.0, 3.0] }
  ]).setParticles(renderer.createResource("PARTICLE_EMITTER", "fiskheroes:impact_charged_beam"));
  stuff.bindFlightTrail(renderer, "skyhighocs:ecolyte_spark_flight");
  //Shock
  shock = stelar.initHandThing(renderer, "shock", 2, 2);
  shockPredation = stelar.initHandThing(renderer, "shock_wave_changing", 0, 2);
  //Arc
  arc = stelar.initHandThing(renderer, "arc", 2, 2);
  arcPredation = stelar.initHandThing(renderer, "arc_wave_changing", 0, 2);
  //Hail cannon
  overCharge = stelar.initHandThing(renderer, "over_charge", 2, 3);
  overChargePredation = stelar.initHandThing(renderer, "over_charge_wave_changing", 0, 2);
  //Taser
  taserMain = renderer.createEffect("fiskheroes:shield");
  taserMain.texture.set("taser");
  taserMain.anchor.set("rightArm");
  taserMain.setRotation(0.0, 0.0, 0.0).setCurve(0.0, 0.0).setOffset(1.0, 12.5, 0.0);
  taserMain.large = true;
  taserArc = renderer.createEffect("fiskheroes:shield");
  taserArc.texture.set(null, "taser_arc");
  taserArc.anchor.set("rightArm");
  taserArc.setRotation(0.0, 0.0, 0.0).setCurve(0.0, 0.0).setOffset(1.0, 14.5, 0.0);
  taserArc.large = true;
  taserWaveChanging = renderer.createEffect("fiskheroes:shield");
  taserWaveChanging.texture.set(null, "taser_wave_changing_lights");
  taserWaveChanging.anchor.set("rightArm");
  taserWaveChanging.setRotation(0.0, 0.0, 0.0).setCurve(0.0, 0.0).setOffset(1.0, 12.5, 0.0);
  taserWaveChanging.large = true;
  taser = stelar.initHandThing(renderer, "taser", 2, 0);
  taserPredation = stelar.initHandThing(renderer, "taser_wave_changing", 0, 2);
  //Head
  head = stelar.initHandThing(renderer, "head", 1, 4, 3);
  headWaveChange = stelar.initHandThing(renderer, "head_wave_change", 1, 4, 3);
  headWaveChanging = stelar.initHandThing(renderer, "head_wave_changing", 0, 1, 3);
};

function render(entity, renderLayer, isFirstPersonArm) {
  parent.render(entity, renderLayer, isFirstPersonArm);
  if (!entity.is("DISPLAY") && renderLayer == "CHESTPLATE" && parent.hasEMBeing(entity)) {
    if (entity.getInterpolatedData("skyhighocs:dyn/wave_changing_timer") == 1 && entity.getInterpolatedData("fiskheroes:mask_open_timer2") > 0) {
      maskWaveChangingLights.render();
      mask.render();
    };
    if (entity.getInterpolatedData("skyhighocs:dyn/wave_changing_timer") > 0) {
      if (entity.getInterpolatedData("skyhighocs:dyn/wave_changing_timer") < 1 && entity.getHeldItem().isEmpty()) {
        headWaveChange.render();
      };
      if (entity.getInterpolatedData("skyhighocs:dyn/wave_changing_timer") == 1 && (entity.getInterpolatedData("skyhighocs:dyn/taser_timer") < 1) && (entity.getInterpolatedData("skyhighocs:dyn/shock_timer") < 1) && (entity.getInterpolatedData("skyhighocs:dyn/arc_timer") < 1) && (entity.getInterpolatedData("skyhighocs:dyn/over_charge_timer") < 1)) {
        head.render();
      };
      if (((entity.getInterpolatedData("skyhighocs:dyn/wave_changing_timer") < 1 && entity.getHeldItem().isEmpty()) || entity.getInterpolatedData("skyhighocs:dyn/wave_changing_timer") == 1) && (entity.getInterpolatedData("skyhighocs:dyn/taser_timer") < 1) && (entity.getInterpolatedData("skyhighocs:dyn/shock_timer") < 1) && (entity.getInterpolatedData("skyhighocs:dyn/arc_timer") < 1) && (entity.getInterpolatedData("skyhighocs:dyn/over_charge_timer") < 1)) {
        headWaveChanging.render();
      };
    };
    if (entity.getInterpolatedData("skyhighocs:dyn/wave_changing_timer") == 1 && entity.getInterpolatedData("skyhighocs:dyn/taser_timer") > 0) {
      taserWaveChanging.render();
      taserMain.render();
      taser.render();
      taserPredation.render();
      if (entity.getData("skyhighocs:dyn/taser") && entity.getHeldItem().isEmpty()) {
        taserArc.render();
      };
    };
    if (entity.getInterpolatedData("skyhighocs:dyn/wave_changing_timer") == 1 && entity.getInterpolatedData("skyhighocs:dyn/shock_timer") > 0) {
      shock.render();
      shockPredation.render();
    };
    if (entity.getInterpolatedData("skyhighocs:dyn/wave_changing_timer") == 1 && entity.getInterpolatedData("skyhighocs:dyn/arc_timer") > 0) {
      arc.render();
      arcPredation.render();
    };
    if (entity.getInterpolatedData("skyhighocs:dyn/wave_changing_timer") == 1 && entity.getInterpolatedData("skyhighocs:dyn/over_charge_timer") > 0) {
      overCharge.render();
      overChargePredation.render();
    };
  };
  if (entity.is("DISPLAY")) {
    if ((entity.getUUID() == getID(entity)) && (entity.as("DISPLAY").getDisplayType() == "BOOK_PREVIEW")) {
      head.render();
      maskWaveChangingLights.render();
      mask.render();
    };
    if (parent.hasEMBeing(entity) && (entity.as("DISPLAY").getDisplayType() == "HOLOGRAM" || entity.as("DISPLAY").getDisplayType() == "DISPLAY_STAND" || entity.as("DISPLAY").getDisplayType() == "ITERATOR_PREVIEW")) {
      maskWaveChangingLights.render();
      mask.render();
      if (entity.getHeldItem().isEmpty()) {
        head.render();
      };
    };
  };
};

